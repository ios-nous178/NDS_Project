// ── 3. MDX 이스케이프 ────────────────────────────────────────────────────

/**
 * MDX 는 백틱(inline code) 바깥의 `{ }` 를 JSX 표현식으로, `<` 를 JSX 태그 시작으로
 * 해석한다. Docusaurus 의 MDX 컴파일러는 prettier 보다 엄격하므로, 백틱 바깥의
 * `{ } < >` 를 직접 이스케이프해야 빌드가 깨지지 않는다. 백틱 안은 raw 로 둔다.
 *
 * 짝이 안 맞는 백틱(홀수)은 split(`[^`]*`) 가 코드 스팬으로 인식하지 못해 raw 로
 * 새어 나가 MDX 에서 닫히지 않는 inline-code 를 만든다 → fail-fast 로 막는다.
 */
function escMdx(text, label = "text") {
  const str = String(text);
  if ((str.match(/`/g)?.length ?? 0) % 2 !== 0) {
    throw new Error(
      `[gen-mcp-tools] '${label}' 의 백틱(\`) 개수가 홀수입니다 — 코드 스팬이 닫히지 않아 MDX 가 깨집니다. ` +
        `registry.ts 의 해당 description 을 고치세요: ${str.slice(0, 80)}…`,
    );
  }
  return str
    .split(/(`[^`]*`)/)
    .map((part, i) => {
      if (i % 2 === 1) return part; // inside backticks → raw
      return part.replace(/[{}]/g, "\\$&").replace(/</g, "&lt;").replace(/>/g, "&gt;");
    })
    .join("");
}

// ── 4. 렌더링 ────────────────────────────────────────────────────────────

function typeLabel(prop) {
  if (Array.isArray(prop.enum)) {
    return prop.enum.map((v) => `\`${v}\``).join(" \\| ");
  }
  if (prop.type === "array") {
    const inner = prop.items?.type ?? "any";
    return `\`${inner}[]\``;
  }
  return `\`${prop.type ?? "any"}\``;
}

function renderParams(tool) {
  const props = tool.inputSchema?.properties ?? {};
  const names = Object.keys(props);
  if (names.length === 0) return ["_인자 없음._", ""];
  const required = new Set(tool.inputSchema?.required ?? []);
  const lines = [];
  for (const name of names) {
    const prop = props[name];
    const req = required.has(name) ? " · **필수**" : "";
    const desc = prop.description ? ` — ${escMdx(prop.description, `${tool.name}.${name}`)}` : "";
    lines.push(`- \`${name}\` ${typeLabel(prop)}${req}${desc}`);
  }
  lines.push("");
  return lines;
}

function renderOverviewTable(tools) {
  const byName = new Map(tools.map((t) => [t.name, t]));
  const rows = [["카테고리", "개수", "도구"]];
  for (const cat of CATEGORIES) {
    const list = cat.tools.map((n) => `\`${n}\``).join(" · ");
    rows.push([cat.title, String(cat.tools.length), list]);
  }
  rows.push(["**합계**", `**${tools.length}**`, ""]);
  // 셀 내용은 우리가 통제(파이프 없음) → 단순 렌더 후 prettier 가 정렬 패딩.
  const header = `| ${rows[0].join(" | ")} |`;
  const sep = `| ${rows[0].map(() => "---").join(" | ")} |`;
  const body = rows.slice(1).map((r) => `| ${r.join(" | ")} |`);
  void byName;
  return [header, sep, ...body].join("\n");
}

function renderFlows() {
  const lines = ["## 호출 흐름 치트시트", ""];
  for (const flow of FLOWS) {
    lines.push(`### ${escMdx(flow.title)}`, "", "```");
    flow.steps.forEach((step, i) => {
      const note = step.note ? `   (${step.note})` : "";
      lines.push(`${i + 1}. ${step.call}${note}`);
    });
    lines.push("```", "");
  }
  return lines.join("\n");
}

function validateCuration(tools) {
  const allNames = new Set(tools.map((t) => t.name));
  const categorized = new Set();
  for (const cat of CATEGORIES) {
    for (const n of cat.tools) {
      if (!allNames.has(n)) {
        throw new Error(
          `[gen-mcp-tools] 카테고리 '${cat.title}' 가 registry 에 없는 도구 '${n}' 를 참조합니다. ` +
            `CATEGORIES 를 정리하세요.`,
        );
      }
      if (categorized.has(n)) {
        throw new Error(`[gen-mcp-tools] 도구 '${n}' 가 두 카테고리에 중복 분류됐습니다.`);
      }
      categorized.add(n);
    }
  }
  for (const n of allNames) {
    if (!categorized.has(n)) {
      throw new Error(
        `[gen-mcp-tools] 새 도구 '${n}' 가 어떤 카테고리에도 없습니다. ` +
          `scripts/generate-mcp-tools-reference.mjs 의 CATEGORIES 에 추가하세요.`,
      );
    }
  }
  for (const flow of FLOWS) {
    for (const step of flow.steps) {
      if (step.tool && !allNames.has(step.tool)) {
        throw new Error(
          `[gen-mcp-tools] 치트 흐름 '${flow.title}' 가 registry 에 없는 도구 '${step.tool}' 를 참조합니다.`,
        );
      }
    }
  }
}

