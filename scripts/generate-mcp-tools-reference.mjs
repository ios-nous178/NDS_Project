/**
 * `const TOOLS = [ ... ];` 의 대괄호 블록을 문자열/이스케이프를 인지하며 균형 스캔으로
 * 잘라내고, 순수 리터럴이므로 new Function 으로 평가한다. (TS 컴파일 불필요)
 */
function extractTools(src) {
  const marker = "const TOOLS = [";
  const markerIdx = src.indexOf(marker);
  if (markerIdx < 0) {
    throw new Error(`[gen-mcp-tools] '${marker}' 를 ${SSOT_REL} 에서 찾지 못했습니다.`);
  }
  const openIdx = src.indexOf("[", markerIdx);
  let depth = 0;
  let inStr = null;
  let esc = false; // 문자열 안에서 직전 문자가 escape(\) 였는지 — 연속 백슬래시도 정확히 카운트
  let endIdx = -1;
  for (let i = openIdx; i < src.length; i++) {
    const ch = src[i];
    if (inStr) {
      // 단일 문자 lookback(prev !== "\\")은 `\\"`(escape 된 백슬래시 + 닫는 따옴표)를
      // escape 된 따옴표로 오인한다. 명시적 escape 상태로 짝수/홀수 백슬래시를 구분한다.
      if (esc) {
        esc = false;
        continue;
      }
      if (ch === "\\") {
        esc = true;
        continue;
      }
      if (ch === inStr) inStr = null;
      continue;
    }
    if (ch === '"' || ch === "'" || ch === "`") {
      inStr = ch;
      continue;
    }
    if (ch === "[") depth++;
    else if (ch === "]") {
      depth--;
      if (depth === 0) {
        endIdx = i;
        break;
      }
    }
  }
  if (endIdx < 0) {
    throw new Error(`[gen-mcp-tools] TOOLS 배열의 닫는 대괄호를 찾지 못했습니다.`);
  }
  const literal = src.slice(openIdx, endIdx + 1);
  let tools;
  try {
    tools = new Function(`return (${literal});`)();
  } catch (e) {
    throw new Error(
      `[gen-mcp-tools] TOOLS 리터럴 평가 실패 — registry.ts 의 TOOLS 가 순수 데이터 리터럴이 ` +
        `아닌 표현(변수 참조/함수 호출 등)을 포함하면 이 생성기는 동작하지 않습니다.\n  ${e.message}`,
    );
  }
  if (!Array.isArray(tools) || tools.length === 0) {
    throw new Error("[gen-mcp-tools] TOOLS 가 비어 있거나 배열이 아닙니다.");
  }
  for (const t of tools) {
    if (!t || typeof t.name !== "string" || typeof t.description !== "string") {
      throw new Error("[gen-mcp-tools] 각 도구는 name/description 문자열을 가져야 합니다.");
    }
  }
  return tools;
}

