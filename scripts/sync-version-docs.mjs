#!/usr/bin/env node
/**
 * sync-version-docs.mjs — packages/{mcp,react,icons,tokens}/package.json → docs version tables
 *
 * Usage:
 *   node scripts/sync-version-docs.mjs           # write (default)
 *   node scripts/sync-version-docs.mjs --check   # CI lint: exit 1 if out of sync
 *
 * Updates the BEGIN/END version-table marker block in:
 *   - docs/HTML_MOCKUP_GUIDE.md
 *   - docs/NUDGE_EAP_DS_MCP_USAGE.md
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { execSync } from "node:child_process";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const mode = process.argv.includes("--check") ? "check" : "write";

const PACKAGES = [
  { label: "MCP 도우미", dir: "packages/mcp" },
  { label: "DS React 컴포넌트", dir: "packages/react" },
  { label: "DS 아이콘", dir: "packages/icons" },
  { label: "DS 디자인 토큰", dir: "packages/tokens" },
];

const TARGETS = ["docs/HTML_MOCKUP_GUIDE.md", "docs/NUDGE_EAP_DS_MCP_USAGE.md"];

const MARKER_BEGIN =
  "<!-- BEGIN: version-table (auto-generated by scripts/sync-version-docs.mjs — do not edit manually) -->";
const MARKER_END = "<!-- END: version-table -->";

// ── Collect versions ────────────────────────────────────────

const versions = PACKAGES.map((p) => {
  const pkgPath = path.join(ROOT, p.dir, "package.json");
  if (!fs.existsSync(pkgPath)) {
    throw new Error(`package.json not found: ${pkgPath}`);
  }
  const j = JSON.parse(fs.readFileSync(pkgPath, "utf-8"));
  return { ...p, version: j.version };
});

const todayDate = new Date().toISOString().slice(0, 10);

// Last commit date that touched any of the tracked packages — falls back to today.
// If there are *staged* changes under packages/, that means a packages-touching commit
// is in flight right now (pre-commit hook) — use today's date so the in-flight commit
// itself carries the up-to-date stamp.
let lastUpdate = todayDate;
try {
  const trackedPaths = PACKAGES.map((p) => p.dir).join(" ");
  const staged = execSync("git diff --cached --name-only", {
    cwd: ROOT,
    encoding: "utf-8",
  });
  const hasStagedPackageChange = staged
    .split("\n")
    .some((f) => PACKAGES.some((p) => f.startsWith(`${p.dir}/`)));

  if (hasStagedPackageChange) {
    lastUpdate = todayDate;
  } else {
    const out = execSync(`git log -1 --format=%cs -- ${trackedPaths}`, {
      cwd: ROOT,
      encoding: "utf-8",
    }).trim();
    if (out) lastUpdate = out;
  }
} catch {
  // git not available — keep today
}

// ── Render block ────────────────────────────────────────────

// markdownlint-cli2 --fix pads table cells by *visual width* (CJK & emoji = 2 cols).
// We mirror that here so the script's output is already canonical — otherwise the
// pre-commit hook keeps re-flowing the table and `--check` flaps.
function visualWidth(s) {
  let w = 0;
  for (const ch of s) {
    const cp = ch.codePointAt(0);
    const wide =
      (cp >= 0x1100 && cp <= 0x115f) ||
      (cp >= 0x2e80 && cp <= 0xa4cf) ||
      (cp >= 0xac00 && cp <= 0xd7a3) ||
      (cp >= 0xf900 && cp <= 0xfaff) ||
      (cp >= 0xfe30 && cp <= 0xfe4f) ||
      (cp >= 0xff00 && cp <= 0xff60) ||
      (cp >= 0xffe0 && cp <= 0xffe6) ||
      (cp >= 0x1f300 && cp <= 0x1f9ff);
    w += wide ? 2 : 1;
  }
  return w;
}

function padCell(text, width) {
  return text + " ".repeat(Math.max(0, width - visualWidth(text)));
}

function renderTable(rows) {
  const colCount = rows[0].length;
  const widths = new Array(colCount).fill(0);
  for (const row of rows) {
    for (let i = 0; i < colCount; i++) {
      widths[i] = Math.max(widths[i], visualWidth(row[i]));
    }
  }
  const fmtRow = (row) => `| ${row.map((c, i) => padCell(c, widths[i])).join(" | ")} |`;
  const sep = `| ${widths.map((w) => "-".repeat(w)).join(" | ")} |`;
  return [fmtRow(rows[0]), sep, ...rows.slice(1).map(fmtRow)].join("\n");
}

function renderBlock() {
  const tableRows = [
    ["항목", "버전"],
    ...versions.map((p) => [p.label, `**${p.version}**`]),
    ["마지막 업데이트", `**${lastUpdate}**`],
  ];
  return [
    MARKER_BEGIN,
    "",
    `## 📌 현재 버전 (${todayDate} 기준)`,
    "",
    renderTable(tableRows),
    "",
    MARKER_END,
  ].join("\n");
}

// ── Apply / check ───────────────────────────────────────────

const blockRe = new RegExp(`${escapeRegex(MARKER_BEGIN)}[\\s\\S]*?${escapeRegex(MARKER_END)}`);

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

let driftCount = 0;
const newBlock = renderBlock();

for (const rel of TARGETS) {
  const filePath = path.join(ROOT, rel);
  if (!fs.existsSync(filePath)) {
    console.error(`✗ ${rel}: not found`);
    process.exit(1);
  }

  const content = fs.readFileSync(filePath, "utf-8");

  if (!blockRe.test(content)) {
    console.error(`✗ ${rel}: BEGIN/END marker not found. Add the marker block manually first.`);
    process.exit(1);
  }

  const updated = content.replace(blockRe, newBlock);

  if (updated === content) {
    console.log(`✓ ${rel}: already up to date`);
    continue;
  }

  driftCount++;

  if (mode === "check") {
    console.error(`✗ ${rel}: out of sync`);
    continue;
  }

  fs.writeFileSync(filePath, updated);
  console.log(`✓ ${rel}: updated`);
}

if (mode === "check") {
  if (driftCount > 0) {
    console.error(`\n${driftCount} doc(s) out of sync. Run \`pnpm sync:docs\` to fix.`);
    process.exit(1);
  }
  console.log("\nAll docs in sync.");
}
